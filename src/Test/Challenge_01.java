package Test;

// Write a Java program that demonstrates proper variable declaration and initialization for different data types.
// Integer: 25 Double: 3.14 Boolean: true Character: A
public class Challenge_01 {
    public static void main(String[] args) {
        // Initializing Variables
        int number=25;
        Double number2=3.14;
        Boolean check=true;
        char character='A';

        // Outputs
        System.out.println("Integer :"+" "+number);
        System.out.println("Double :"+" "+number2);
        System.out.println("Boolean :"+" "+check);
        System.out.println("Character :"+" "+character);
    }
}
