package Loops;

public class for_exp_04 {
    public static void main(String[] args) {
        for (long i = 1l; i < 10; i++) {
            System.out.println(i);
        }
        for (float f = 0.0f; f < 10.67; f++) {
            System.out.println("Hi,Float -> " + f);

        }

        int i = 10;
        for (; i > 0; i--) {
            System.out.println(i);
        }
    }
}
