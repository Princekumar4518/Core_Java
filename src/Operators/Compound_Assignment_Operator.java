package Operators;

public class Compound_Assignment_Operator {
    public static void main(String[] args) {

        // Compound Assignment Operators
        int age = 10;
        age += 10;   // age = age+10 =20
        age /=10; // age = age/10 =2
        age *=10; // age=age*10 =20
        System.out.println(age);
    }
}
