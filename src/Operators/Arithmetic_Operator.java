package Operators;

public class Arithmetic_Operator {
    public static void main(String[] args) {


        // Arithmetic Operators
        //      + (Addition)
        //      - (Subtraction)
        //      * (Multiplication)
        //      / (Division)
        //       % (Modulus)= Remainder


        int a=21;
        int b=6;
        float c=2;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);  //it will print quotient
        System.out.println(a%b); // it will print remainder
        System.out.println(a/c);

    }
}
