package Operators;

public class Relational_Operator {
    public static void main(String[] args) {
        // <  Less Than
        // < =  -> Less than or equal to
        // > Greater
        // > = Greater or equal
        // == ->  Equal to (but checking)
        // != -> Not equal

        // All of them will result boolean output.

        int a = 10;
        int b = 30;
        boolean c = a > b; // // 10> 30
        System.out.println(c);

        int Prince_age= 23;
        int Tushar_age= 28;

        boolean result = Tushar_age >= Prince_age;
        System.out.println(result);

        // Prince_age> Tushar_ag or Tushar_ag = Prince_age

    }
}
